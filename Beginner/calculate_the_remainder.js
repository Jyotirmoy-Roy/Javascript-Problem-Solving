/* 
Q10. Calculate the remainder
Given two numbers, return their remainder when divided by each other


calcRemainder(4, 2) -> 0
calcRemainder(7, 8) -> 7 
calcRemainder(9, 8) -> 1

*/

// Solution

function calcRemainder(n1, n2) {
  return n1 % n2;
}

let result;

result = calcRemainder(4, 2);
result = calcRemainder(7, 8);
result = calcRemainder(9, 8);

console.log(result);



